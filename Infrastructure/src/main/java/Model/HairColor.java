package Model;

/**
 * Перечисление {@code HairColor} представляет возможные цвета волос.
 *
 * Включает три значения:
 * <ul>
 *   <li>{@link #BLACK} - Черный цвет волос</li>
 *   <li>{@link #WHITE} - Белый цвет волос</li>
 *   <li>{@link #OTHER} - Другие цвета волос</li>
 * </ul>
 *
 * Пример использования:
 * <pre>
 *     HairColor color = HairColor.BLACK;
 * </pre>
 */
public enum HairColor {
    /**
     * Черный цвет волос
     */
    BLACK,

    /**
     * Белый цвет волос
     */
    WHITE,

    /**
     * Другие цвета волос
     */
    OTHER
}
